a = int(input("Enter first number: "))
op = input("Enter operator (+, -, *, /): ")
b = int(input("Enter second number: "))

if op == "+":
    print("Result:", a + b)
elif op == "-":
    print("Result:", a - b)
elif op == "*":
    print("Result:", a * b)
elif op == "/":
    print("Result:", a / b)
